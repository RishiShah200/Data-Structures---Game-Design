public class Book {
    private String author;
    private String title;
    private double rating;
    private int numberOfRatings;
    private int votes;
    private int score;
    public Book(String author, String title, double rating, int numberOfRatings, int votes, int score){
        this.author = author;
        this.title = title;
        this.rating = rating;
        this.numberOfRatings = numberOfRatings;
        this.votes = votes;
        this.score = score;
    }


}
